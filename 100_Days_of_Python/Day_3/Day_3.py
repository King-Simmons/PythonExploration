print("Welcome to Treasure Island.")
print("Your mission is to find the treasure.")
def play_game():
    choice1 = input("left or right?")
    if choice1.casefold() != "left".casefold():
        print("Fall into a hole. Game Over.")
        return
    choice2 = input("swim or wait")
    if choice2.casefold() != "wait".casefold():
        print("Attacked by trout. Game Over.")
        return
    choice3 = input("Which color door? (Red, Blue, Yellow)")
    if choice3.casefold() == "red".casefold():
        print("Burned by fire. Game Over.")
    elif choice3.casefold() == "blue".casefold():
        print("Eaten by beasts. Game Over.")
    elif choice3.casefold() == "yellow".casefold():
        print("You win!")
    else:
        print("Game Over.")
    return
play_game()